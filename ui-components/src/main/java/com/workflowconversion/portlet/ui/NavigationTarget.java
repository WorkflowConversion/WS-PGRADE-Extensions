package com.workflowconversion.portlet.ui;

/**
 * Enum designed to name the navigation items.
 * 
 * @author delagarza
 *
 */
public enum NavigationTarget {
	/**
	 * Import a single workflow.
	 */
	ImportWorkflow,
	/**
	 * Manage imported workflows.
	 */
	ManageWorkflows,
	/**
	 * Import applications using a CSV file.
	 */
	BulkImportApplications,
	/**
	 * Manage applications.
	 */
	ManageApplications,
	/**
	 * Help display.
	 */
	Help,
	/**
	 * About window.
	 */
	About;
}
